function add (a, b) {
  var answer = a + b
  return answer
}

function subtract (a, b) {
  var answer = a - b
  return answer
}

function multiply (a, b) {
  var answer = a*b
  return answer
}

function divide (a, b) {
  var answer = a/b
  return answer
}

function inc (n) {
  n++
  return n
}

function dec (n) {
  n--
  return n
}

function makeInt (n) {
  var result = parseInt(n, 10)
  return result
}

function preserveDecimal (n) {
  var result = parseFloat(n)
  return result
}
