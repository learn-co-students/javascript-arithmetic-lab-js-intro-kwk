function add(a,b){
  return a+b
}
function subtract(a,b){
  return a-b
}
function multiply(a,b){
  return a*b
}
function divide(a,b){
  return a/b
}
function inc(n){
  n++
  return n
}
function dec(m){
  m--
  return m
}
function makeInt(str){
  return parseInt(str,10)
}
function preserveDecimal(str){
  return parseFloat(str)
}